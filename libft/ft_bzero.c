#include <stdio.h>
#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*u;

	u = (unsigned char *)s;
	while (n > 0)
	{
		u[n - 1] = '\0';
		n--;
	}
}
