#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	while (str[i] != '\0')
	{
		if (str[i] == (char)c)
			break ;
		i++;
	}
	if (str[i] == '\0' && (char)c != '\0')
		return (NULL);
	return (str + i);
}
